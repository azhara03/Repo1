    <h4 class="mt-0">Город Бишкек</h4>
Бишкек — столица Киргизии и крупнейший город страны. Составляет особую административную единицу. 
Город расположен на севере Киргизской Республики, в Чуйской долине, у предгорьев Тянь-Шаня, в 40 км 
севернее Киргизского хребта, в 25 км от границы с Казахстаном. Бишкек, являясь центром международного 
туризма в Кыргызстане, часто служит перевалочным пунктом и местом отдыха на пути к озеру Иссык-Куль 
или горам Тянь-Шаня и в то же время может предложить туристам большое количество достопримечательностей.

    <h4 class="mt-0 mb-1">История</h4>
    Город Бишкек был известен с VII в. как городище Джуль (Кузнечная крепость). 
    В 1825 году, была основана кокандская крепость Пишпек, размещавшая крупнейший 
    гарнизон в Чуйской долине. Дважды (4 сентября 1860 и 24 октября 1862) был взят русскими войсками. 
     ноябре 1862 крепость была разрушена, а на её месте двумя годами позже был установлен казачий пикет, 
     затем здесь стал собираться базар. В 1868 было основано селение Пишпек. 29 апреля 1878 в связи с 
     переводом в Пишпек центра уезда получил статус города.

     С октября 1924 становится административным центром Кара-Киргизской автономной области. 
     С мая 1925 — административный центр Киргизской автономной области. 
     В 1926 переименован во Фрунзе в честь уроженца города, советского военачальника Михаила Фрунзе. 
     С 1936 Фрунзе — столица Киргизской ССР. В 1991, после провозглашения независимости 
     Киргизской республики, город переименован в Бишкек. По одной версии новое название происходит от 
     мифического героя Бишкек-Батыра, открывшим на месте города большой базар. По другой — от созвучия 
     слов Пишпек (первое название) и Бишкек (кухонная утварь, палка для размешивания кумыса).  
